for (var i = 1; i <= 10; i++) { // Vòng lặp chạy từ 1 đến 10, biến i là số bảng cần in.
  for (var j = 1; j <= 10; j++) { // Vòng lặp con chạy từ 1 đến 10, biến j là số nhân của bảng cửu chương.
    document.write(i + " x " + j + " = " + i*j + "<br>"); // In ra bảng cửu chương.
  }
  document.write("<br>"); // In ra dòng trống sau mỗi bảng cửu chương.
}
